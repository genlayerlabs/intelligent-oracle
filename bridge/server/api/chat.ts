import { convertToCoreMessages, streamText } from "ai";
import { createOpenAI } from "@ai-sdk/openai";

export default defineLazyEventHandler(async () => {
  const openai = createOpenAI({
    baseURL: useRuntimeConfig().llmBaseUrl,
    apiKey: useRuntimeConfig().llmApiKey,
  });

  return defineEventHandler(async (event: any) => {
    // Add CORS headers
    setResponseHeaders(event, {
      "Access-Control-Allow-Origin": "*",
      "Access-Control-Allow-Methods": "*",
      "Access-Control-Allow-Headers": "Content-Type",
    });
    // Handle OPTIONS request for CORS preflight
    if (event.method === "OPTIONS") {
      return "OK";
    }
    // Extract the `messages` from the body of the request
    const { messages } = await readBody(event);
    const conversationMessages = [{ role: "user", content: initialPrompt }, ...messages.slice(1)];
    console.log("🚀 ~ returndefineEventHandler ~ conversationMessage:", conversationMessages);

    // Call the language model
    const result = await streamText({
      model: openai(useRuntimeConfig().llmApiModel),
      messages: convertToCoreMessages(conversationMessages),

      async onFinish({ text, toolCalls, toolResults, usage, finishReason }) {
        // implement your own logic here, e.g. for storing messages
        // or recording token usage
      },
    });

    // Respond with the stream
    return result.toDataStreamResponse();
  });
});

